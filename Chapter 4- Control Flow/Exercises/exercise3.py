#IF BLOCK
alien_color = 'green'
if alien_color == 'green':
    print("You have just earned yourself 5 points!")
elif alien_color == 'yellow':
    print("You have just earned yourself 10 points!")
else:
    print("You have just earned yourself 15 points!")

#ELIF BLOCK
alien_color = 'yellow'
if alien_color == 'green':
    print("You have just earned yourself 5 points!")
elif alien_color == 'yellow':
    print("You have just earned yourself 10 points!")
else:
    print("You have just earned yourself 15 points!")

#ELSE BLOCK
alien_color = 'red'
if alien_color == 'yellow':
    print("You have just earned yourself 5 points!")
elif alien_color == 'green':
    print("You have just earned yourself 10 points!")
else:
    print("You have just earned yourself 15 points!")