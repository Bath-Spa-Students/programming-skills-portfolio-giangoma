#IF BLOCK
alien_color = 'green'
if alien_color == 'green':
    print("You just shot the alien and gained 5 points!")
else:
    print("You just gained 10 points. Congratulations!")

#ELSE BLOCK
alien_color = 'yellow'
if alien_color == 'red':
    print("You just shot the alien and gained 5 points!")
else:
    print("You just gained 10 points. Congratulations!")

    


